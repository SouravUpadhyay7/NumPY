import numpy as np

a = np.array([1, 2, 3])
b = np.array([[1, 2, 3],[4,5,6]])

print(a)
print(b)
print('-'*10)

# Addition
print(a + 2)
print(a + b)
print('-'*10)


# Subtract
print(a - 2)
print(a - b)
print('-'*10)

# Multiplication
print(a * 2)
print(a * b)
print('-'*10)

# Division
print(a / 2)
print(a / b)
print('-'*10)

# Exponential
print(a ** 2)
print(a ** b)
print('-'*10)

# Modulo
print(a % 2)
print(a % b)
print('-'*10)